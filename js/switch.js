'use strict'
//condicional switch

var edad =70;

switch(edad){
    case 18:
        console.log("Acabas de cumplir la mayoria de edad");
        break;
    case 25:
        console.log("Ya eres un adulto");
        break;
    case 40:
        console.log("Ya estas en la crisis de los 40");
        break;
    case 70:
        console.log("Ya estas anciano");
        break;
    default:
        console.log("Tu edad es neutral");
        break;

}



